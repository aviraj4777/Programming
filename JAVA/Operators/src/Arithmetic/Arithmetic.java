package Arithmetic;
import java.util.*;
import java.lang.*;
public class Arithmetic {
    public static void main (String args[]) {
        int a = 14;
        int b = 5;

        int q = a / b;
        int r = a % b;

        System.out.println(q);
        System.out.println(r);

        int base = 15;
        int height = 2;
        int area = (base * height) / 2;
        System.out.println(area);
    }
}
