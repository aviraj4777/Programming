//Primitive Data Types :- nn bb ss u
let a = null; // null
let b = 345; // number
let c = true; // boolean
let d = BigInt("45445"); // bigInt
let e = "Harry"; // String
let f = Symbol("I am a nice symbol"); // Symbol
let g = undefined; // undefined

console.log(a, b, c, d, e, f, g);

//Non - Primitive Data types :- Objects
const item = {
    "Aviraj" : 1,
    "Abhinav" : 2,
    "Aditya" : 3,
    "Gautam" : 4,
}
console.log(item["Aditya"]);